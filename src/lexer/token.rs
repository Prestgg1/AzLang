#[derive(Debug, PartialEq, Clone)]
pub enum Token {
    Print,
    Input,
    MutableDecl,
    ConstantDecl,
    FunctionDef,
    Conditional,
    Backtick,
    InterpolationStart,
    TemplatePart(String),
    Else,
    ElseIf,
    Drop,
    TypeName(String),
    Loop,
    BigInteger,
    LowInteger,
    Integer,
    String,
    Identifier(String),
    Number(i64),
    StringLiteral(String),
    Operator(String),
    LParen,
    RParen,
    LBrace,
    RBrace,
    Semicolon,
    Colon,
    Comma,
    ListStart,
    ListEnd,
    Return,
    EOF,
}
